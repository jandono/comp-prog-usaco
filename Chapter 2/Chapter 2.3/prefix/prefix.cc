ifstream fin("prefix.in");
ofstream fout("prefix.out");

string primitive;
unordered_set<string> primitives;
string seq, sub_seq;
int MAX_LEN = INT_MIN;
VB dp;

void read_input(){
    while(fin >> primitive && primitive != "."){
        
        MAX_LEN = max(MAX_LEN, (int)primitive.size());
        primitives.insert(primitive);
    }
    
    seq = "";
    while(fin >> sub_seq){
        seq += sub_seq;
    }
}

void solve(){
    int n = seq.size();
    dp.resize(n+1, false);
    dp[0] = true;
    int solution = 0;
    for(int i=0;i<=n;i++){
        
        if(!dp[i]) continue;
        for(int j=1;j<=MAX_LEN;j++){
            
            if(i + j <= n && primitives.count(seq.substr(i, j))){
                dp[i + j] = true;
                solution = max(solution, i + j);
            }
        }
    }
    fout << solution << "\n";
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    read_input();
    solve();
    return 0;
}
